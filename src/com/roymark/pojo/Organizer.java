package com.roymark.pojo;

public class Organizer {
     private String o_photo;
     private String o_name;
     private String o_introduction;
	public String getO_photo() {
		return o_photo;
	}
	public void setO_photo(String o_photo) {
		this.o_photo = o_photo;
	}
	public String getO_name() {
		return o_name;
	}
	public void setO_name(String o_name) {
		this.o_name = o_name;
	}
	public String getO_introduction() {
		return o_introduction;
	}
	public void setO_introduction(String o_introduction) {
		this.o_introduction = o_introduction;
	}
     
}
