package com.roymark.pojo;

public class Fee {
     private Integer f_id;
     private String f_name;
     private  String f_unit;
     private  Integer f_expense;
	public Integer getF_id() {
		return f_id;
	}
	public void setF_id(Integer f_id) {
		this.f_id = f_id;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_unit() {
		return f_unit;
	}
	public void setF_unit(String f_unit) {
		this.f_unit = f_unit;
	}
	public Integer getF_expense() {
		return f_expense;
	}
	public void setF_expense(Integer f_expense) {
		this.f_expense = f_expense;
	}
}
